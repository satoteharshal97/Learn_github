#!/bin/bash

read -p "Enter the single digit number(0 to 5):" day


case $day in
	0) echo "Day is Sunday" ;;
	1) echo "Day is Monday" ;;
	2) echo "Day is Tuesday" ;;
	3) echo "Day is Wednesday" ;;
	4) echo "Day is Thursday" ;;
	5) echo "Day is Friday" ;;
	6) echo "Day is Saturday" ;;
esac
	
	
	
	
	
	
